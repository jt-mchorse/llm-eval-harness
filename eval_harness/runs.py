"""SQLite-backed run history for the regression runner.

A single file at a caller-supplied path holds the entire run history.
The schema is intentionally small — `runs` for aggregate per-run
metrics, `rows` for the per-example judge scores — so consumers can
join on `run_id` and reconstruct enough state to compute deltas
against any prior run.

The harness writes via stdlib `sqlite3` only; no migration framework,
no ORM. `init_db(path)` is idempotent (`CREATE TABLE IF NOT EXISTS`).
Callers who want to use an existing connection can pass it via
`init_db_on(conn)`.

Schema rationale:
- `runs.run_id` is a string UUID generated by the runner so multiple
  machines can write to the same file without an ID collision.
- `started_at` is an ISO-8601 string (not a SQLite-native TIMESTAMP)
  for portability — the harness only needs to sort and display, not
  index date ranges.
- `git_sha` is recorded when the runner can read it; otherwise stored
  as `NULL`. The CI Action (#6) supplies the merge-base SHA.
- `rows.score` is REAL and bounded [0, 1] by the writer (the `Judge`
  layer already clamps); we don't enforce a CHECK constraint so an
  upgrade path is possible without a migration.
"""

from __future__ import annotations

import sqlite3
import uuid
from collections.abc import Sequence
from dataclasses import dataclass
from datetime import UTC, datetime
from pathlib import Path
from typing import Any

SCHEMA = """
CREATE TABLE IF NOT EXISTS runs (
    run_id TEXT PRIMARY KEY,
    started_at TEXT NOT NULL,
    suite TEXT NOT NULL,
    dataset_version TEXT NOT NULL,
    judge_model TEXT,
    judge_kappa REAL,
    mean_score REAL NOT NULL,
    n_rows INTEGER NOT NULL,
    git_sha TEXT
);

CREATE TABLE IF NOT EXISTS rows (
    run_id TEXT NOT NULL,
    example_id TEXT NOT NULL,
    score REAL NOT NULL,
    reasoning TEXT NOT NULL,
    PRIMARY KEY (run_id, example_id),
    FOREIGN KEY (run_id) REFERENCES runs(run_id)
);

CREATE INDEX IF NOT EXISTS idx_runs_suite_started ON runs(suite, started_at);
CREATE INDEX IF NOT EXISTS idx_rows_run_example ON rows(run_id, example_id);
"""


@dataclass(frozen=True)
class StoredRun:
    """Aggregate row from `runs`, plus the per-row scores from `rows`."""

    run_id: str
    started_at: str
    suite: str
    dataset_version: str
    judge_model: str | None
    judge_kappa: float | None
    mean_score: float
    n_rows: int
    git_sha: str | None
    rows: dict[str, tuple[float, str]]  # example_id -> (score, reasoning)


def new_run_id() -> str:
    """Caller-friendly UUID generator. Same generator the runner uses internally."""
    return uuid.uuid4().hex


def utc_now_iso() -> str:
    """ISO-8601 UTC timestamp used by the runner; exposed for tests/replays."""
    return datetime.now(tz=UTC).strftime("%Y-%m-%dT%H:%M:%SZ")


def connect(path: str | Path) -> sqlite3.Connection:
    """Open a connection at `path`. Parent directories are created if missing.

    The connection has `foreign_keys` enabled so the `rows.run_id` FK is
    actually enforced — without the pragma, SQLite silently drops orphaned
    rows references.
    """
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(p))
    conn.execute("PRAGMA foreign_keys = ON;")
    return conn


def init_db(path: str | Path) -> None:
    """Idempotently create the schema at `path`."""
    with connect(path) as conn:
        init_db_on(conn)


def init_db_on(conn: sqlite3.Connection) -> None:
    """Variant of `init_db` for callers that already hold a connection (e.g. tests)."""
    conn.executescript(SCHEMA)
    conn.commit()


def write_run(
    conn: sqlite3.Connection,
    *,
    run_id: str,
    started_at: str,
    suite: str,
    dataset_version: str,
    judge_model: str | None,
    judge_kappa: float | None,
    mean_score: float,
    n_rows: int,
    git_sha: str | None,
    rows: Sequence[tuple[str, float, str]],
) -> None:
    """Persist one run. Atomic — succeeds or rolls back. Duplicate `run_id` raises."""
    try:
        with conn:
            conn.execute(
                """
                INSERT INTO runs (run_id, started_at, suite, dataset_version, judge_model,
                                  judge_kappa, mean_score, n_rows, git_sha)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?);
                """,
                (
                    run_id,
                    started_at,
                    suite,
                    dataset_version,
                    judge_model,
                    judge_kappa,
                    mean_score,
                    n_rows,
                    git_sha,
                ),
            )
            conn.executemany(
                "INSERT INTO rows (run_id, example_id, score, reasoning) VALUES (?, ?, ?, ?);",
                [(run_id, ex_id, score, reasoning) for ex_id, score, reasoning in rows],
            )
    except (
        sqlite3.IntegrityError
    ) as e:  # pragma: no cover - exact message is sqlite-version-specific
        raise ValueError(f"failed to persist run {run_id!r}: {e}") from e


def read_run(conn: sqlite3.Connection, run_id: str) -> StoredRun:
    """Load one run by id. Raises `KeyError` if the id is unknown."""
    cur = conn.execute(
        """
        SELECT run_id, started_at, suite, dataset_version, judge_model,
               judge_kappa, mean_score, n_rows, git_sha
        FROM runs WHERE run_id = ?;
        """,
        (run_id,),
    )
    row = cur.fetchone()
    if row is None:
        raise KeyError(f"no run with id {run_id!r}")
    rows_cur = conn.execute(
        "SELECT example_id, score, reasoning FROM rows WHERE run_id = ?;",
        (run_id,),
    )
    rows_by_id = {ex_id: (score, reasoning) for ex_id, score, reasoning in rows_cur}
    return StoredRun(
        run_id=row[0],
        started_at=row[1],
        suite=row[2],
        dataset_version=row[3],
        judge_model=row[4],
        judge_kappa=row[5],
        mean_score=row[6],
        n_rows=row[7],
        git_sha=row[8],
        rows=rows_by_id,
    )


def latest_run_id_for_suite(
    conn: sqlite3.Connection,
    suite: str,
    *,
    exclude_run_id: str | None = None,
) -> str | None:
    """Return the most-recent `run_id` for `suite`, or None if there are none.

    Sorted by `started_at` descending — the ISO-8601 format is lexicographically
    sortable so a string compare suffices. `exclude_run_id` lets the runner ask
    for "latest other than the run I just inserted," which matters because two
    consecutive runs can share a 1-second-resolution `started_at`.
    """
    if exclude_run_id is None:
        cur = conn.execute(
            "SELECT run_id FROM runs WHERE suite = ? ORDER BY started_at DESC LIMIT 1;",
            (suite,),
        )
    else:
        cur = conn.execute(
            "SELECT run_id FROM runs WHERE suite = ? AND run_id != ? "
            "ORDER BY started_at DESC LIMIT 1;",
            (suite, exclude_run_id),
        )
    row = cur.fetchone()
    return row[0] if row else None


@dataclass(frozen=True)
class RunSummary:
    """One row from `list_runs`: the aggregate fields, no per-example rows."""

    run_id: str
    started_at: str
    suite: str
    dataset_version: str
    judge_model: str | None
    judge_kappa: float | None
    mean_score: float
    n_rows: int
    git_sha: str | None


def list_runs(
    conn: sqlite3.Connection,
    *,
    limit: int = 20,
    suite: str | None = None,
) -> list[RunSummary]:
    """Return up to `limit` most-recent runs, optionally filtered by suite.

    Sorted by `started_at` descending. Used by the `eval-harness list`
    CLI subcommand (#7). Rows are loaded into a list (not yielded) because
    consumers always print or serialize the full set; streaming buys
    nothing at typical history sizes (<= a few thousand runs per DB).
    """
    # Pre-#42 the sign-only `limit <= 0` accepted NaN (NaN <= 0 is false) and
    # floats (`0.5` silently truncates to `0` in SQLite's LIMIT integer
    # coercion → no rows returned; `NaN` propagates into the LIMIT bind and
    # surfaces as a cryptic sqlite3.InterfaceError). Require int + positive.
    if not isinstance(limit, int) or isinstance(limit, bool) or limit <= 0:
        raise ValueError(f"limit must be a positive integer, got {limit!r}")
    query = (
        "SELECT run_id, started_at, suite, dataset_version, judge_model, "
        "       judge_kappa, mean_score, n_rows, git_sha "
        "FROM runs"
    )
    params: tuple[Any, ...]
    if suite is None:
        query += " ORDER BY started_at DESC LIMIT ?;"
        params = (limit,)
    else:
        query += " WHERE suite = ? ORDER BY started_at DESC LIMIT ?;"
        params = (suite, limit)
    cur = conn.execute(query, params)
    return [
        RunSummary(
            run_id=row[0],
            started_at=row[1],
            suite=row[2],
            dataset_version=row[3],
            judge_model=row[4],
            judge_kappa=row[5],
            mean_score=row[6],
            n_rows=row[7],
            git_sha=row[8],
        )
        for row in cur
    ]
